function dictionary_r(text) {
    var x;
    switch (text) {
        case "Cu": x = "铜矿";
            break;
        case "Pb": x = "铅矿";
            break;
        case "Sn": x = "锡矿";
            break;
        case "Ti": x = "钛矿";
            break;
        case "Cr": x = "铬矿";
            break;
        case "Ni": x = "镍矿";
            break;
    }
    return x;
}

